package package8;

/**
 * Interfaz que representa las acciones básicas de un animal.
 * Define métodos para dormir, comer y hacer ruido.
 */
public interface Animal {
	
    /**
     * Método que representa el acto de dormir de un animal.
     */
    public void dormir();

    /**
     * Método que representa el acto de comer de un animal.
     */
    public void comer();

    /**
     * Método que representa el ruido característico que hace un animal.
     */
    public void hacerRuido();
}
